// # Using the dayOfWeek variable, complete the switch statement

let dayOfWeek = "Monday";

switch (dayOfWeek) {
    case "Monday":
        console.log("It's Monday. Start of the week!");
        break;

    case "Tuesday":
        console.log("It's Tuesday.");
        break;

    case "Wednesday":
        console.log("It's Wednesday.");
        break;

    case "Thursday":
        console.log("It's Thursday.");
        break;

    case "Friday":
        console.log("It's Friday. Weekend is almost here!");
        break;

    case "Saturday":
        console.log("It's Saturday, time to relax!");
        break;

    case "Sunday":
        console.log("It's Sunday, Aw man tomorrow is Monday again.");
        break;

    default:
        console.log("Invalid day!");
        break;
}